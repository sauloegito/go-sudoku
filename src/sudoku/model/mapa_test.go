func checkMapa(m *Mapa, t *testing.T) {
	for row := byte(0); row < 9; row++ {
		for col := byte(0); col < 9; col++ {
			if m.Cells[col][row].Get() == 0 {
				t.Error("Regras insuficientes para solução do Sudoku!")
				return
			}
		}
	}
}

